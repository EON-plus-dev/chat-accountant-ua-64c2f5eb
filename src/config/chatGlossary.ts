// Patterns: "що таке X", "що означає X", "поясни X", "а що це X", "що за X", "розкажи про X", "X -- це що"
const QUESTION_PATTERNS: RegExp[] = [
  /(?:що\s+таке|що\s+означає|поясни|а?\s*що\s+це|що\s+за|розкажи\s+про)\s+(.+)/i,
  /(.+?)\s*[-–—]+\s*це\s+що\??/i,
  /(.+?)\s*це\s+що\s*таке\??/i,
];

function normalizeKey(raw: string): string {
  return raw
    .replace(/[«»""„?!.,;:()]/g, "")
    .trim()
    .toLowerCase();
}

export function matchGlossary(
  text: string
): GlossaryEntry | null {
  const trimmed = text.trim();
  for (const pattern of QUESTION_PATTERNS) {
    const m = trimmed.match(pattern);
    if (m && m[1]) {
      const key = normalizeKey(m[1]);
      if (GLOSSARY[key]) return GLOSSARY[key];
      // Try partial match (e.g. "єдиний податок" in "що таке єдиний податок для ФОП")
      for (const [gKey, entry] of Object.entries(GLOSSARY)) {
        if (key.includes(gKey) || gKey.includes(key)) return entry;
      }
    }
  }
  return null;
}
