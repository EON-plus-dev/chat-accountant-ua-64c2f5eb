export const RULE_KEYWORD_STOPWORDS = new Set<string>([
  "КОМІСІЯ", "ЗА", "ВІД", "ОПЛАТА", "ПЛАТІЖ", "ПЕРЕКАЗ", "БАНК",
  "ДОГОВОРУ", "ДОГОВІР", "РАХУНОК", "РАХ", "НА", "ДЛЯ", "ЧЕРЕЗ",
  "ПОВЕРНЕННЯ", "ПОВЕРН", "СУМА", "ГРН", "UAH", "USD", "EUR",
  "ТОВ", "ФОП", "ПП", "ПРАТ", "ВІДПОВІДНО", "ПДВ", "БЕЗ",
]);

export const extractRuleKeywords = (
  description: string,
  contractor?: string
): string[] => {
  const cleaned = description.replace(/[\d.,]+\s*(грн|uah|usd|eur)/gi, "").trim();
  const words = cleaned
    .split(/\s+/)
    .map((w) => w.replace(/[^\p{L}\p{N}-]/gu, ""))
    .filter((w) => w.length >= 3)
    .map((w) => w.toUpperCase())
    .filter((w) => !RULE_KEYWORD_STOPWORDS.has(w));

  if (words.length >= 2) return words.slice(0, 2);
  if (words.length === 1) return words;
  // Fallback: use contractor as seed
  if (contractor) {
    const cWords = contractor
      .split(/\s+/)
      .filter((w) => w.length >= 3)
      .map((w) => w.toUpperCase())
      .filter((w) => !RULE_KEYWORD_STOPWORDS.has(w));
    if (cWords.length) return cWords.slice(0, 2);
  }
  return words;
};

