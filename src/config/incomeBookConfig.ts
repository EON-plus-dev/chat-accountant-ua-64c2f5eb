// Helper function to aggregate records by month dynamically
export function aggregateRecordsByMonth(records: IncomeBookRecord[]): MonthlyAggregate[] {
  const byMonth: Record<string, IncomeBookRecord[]> = {};
  
  records.forEach(r => {
    const month = r.date.slice(0, 7); // "2025-03"
    if (!byMonth[month]) byMonth[month] = [];
    byMonth[month].push(r);
  });
  
  return Object.entries(byMonth).map(([month, recs]) => {
    const [year, monthNum] = month.split("-");
    const monthLabel = monthLabelsMap[monthNum] || monthNum;
    
    // Calculate aggregates by payment type
    const calcByType = (type: PaymentType) => ({
      income: recs
        .filter(r => r.paymentType === type && r.status !== "return")
        .reduce((sum, r) => sum + r.amount, 0),
      return: recs
        .filter(r => r.paymentType === type && r.status === "return")
        .reduce((sum, r) => sum + (r.returnAmount || 0), 0),
    });
    
    return {
      month,
      label: `${monthLabel} ${year}`,
      cash: calcByType("cash"),
      bank: calcByType("bank"),
      card: calcByType("card"),
      prro: calcByType("prro"),
      totalIncome: recs
        .filter(r => r.status !== "return")
        .reduce((sum, r) => sum + r.amount, 0),
      totalReturn: recs
        .filter(r => r.status === "return")
        .reduce((sum, r) => sum + (r.returnAmount || 0), 0),
      inIncomeBook: recs.reduce((sum, r) => sum + r.inIncomeBook, 0),
      hasIssues: recs.some(r => r.status === "needs-clarification"),
      recordsCount: recs.length,
    };
  }).sort((a, b) => a.month.localeCompare(b.month));
}

export interface IncomeBookRecord {
  id: string;
  date: string; // ISO date
  description: string;
  contractor?: string;
  contractorCode?: string; // ЄДРПОУ (8 цифр) або ІПН (10 цифр)
  contractorIban?: string; // UA... IBAN контрагента
  amount: number; // сума операції (грн)
  returnAmount?: number; // сума повернення
  inIncomeBook: number; // сума в дохід книги
  paymentType: PaymentType;
  source: DataSource;
  status: IncomeRecordStatus;
  issueType?: IssueType; // Тип проблеми для needs-clarification
  aiNote?: string;
  txnId?: string;
  linkedReturnId?: string; // для зв'язаних повернень
  relatedDocument?: {
    type: "invoice" | "act" | "contract" | "check" | "receipt" | "other";
    number: string;
    date: string;
    /** Optional — populated when user attaches a file inline (data URL kept in memory only) */
    fileName?: string;
    fileMime?: string;
    fileDataUrl?: string;
  };
  valuationDate?: string; // Дата валютування (для банку)
  commission?: number; // Комісія ПС (для way4pay, liqpay)
  // Sync with Document Flow
  documentFlowId?: string; // Посилання на документ у документообігу
  documentNumber?: string; // Номер документа для швидкого доступу
  // Auto-categorization
  categoryCode?: string; // Код категорії доходу/витрати
  categorySource?: "auto" | "manual" | "rule" | "auto-internal-transfer"; // Джерело категоризації
  matchedRuleId?: string; // ID правила, що спрацювало
  categoryConfirmed?: boolean; // Чи підтверджена категорія
}

export interface MonthlyAggregate {
  month: string; // "2025-01"
  label: string; // "Січень 2025"
  cash: { income: number; return: number };
  bank: { income: number; return: number };
  card: { income: number; return: number };
  prro: { income: number; return: number };
  totalIncome: number;
  totalReturn: number;
  inIncomeBook: number;
  hasIssues: boolean;
  issueNote?: string;
  recordsCount: number;
}

