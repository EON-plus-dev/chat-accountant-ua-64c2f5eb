import { statusRowStyles, type StatusRowStyle } from "@/config/semanticStyles";

/**
 * Get unified row styles for Income Book records
 * Implements severity escalation: critical issueType upgrades border from amber to red
 * 
 * @param status - Record status (income, needs-clarification, return, not-income)
 * @param issueType - Optional issue type for needs-clarification records
 * @returns StatusRowStyle with synchronized border, badge, and background
 */
export const getRowStyles = (
  status: IncomeRecordStatus, 
  issueType?: IssueType
): StatusRowStyle => {
  // Severity escalation for needs-clarification with critical issueType
  if (status === "needs-clarification" && issueType) {
    const issuePriority = issueTypeConfig[issueType]?.priority;
    if (issuePriority === 1) {
      // Critical priority → red border, amber badge
      return statusRowStyles["needs-clarification-critical"];
    }
  }
  
  return statusRowStyles[status] || statusRowStyles["neutral"];
};

/**
 * Check if record has critical severity issue
 */
export const hasCriticalIssue = (issueType?: IssueType): boolean => {
  if (!issueType) return false;
  return issueTypeConfig[issueType]?.priority === 1;
};

