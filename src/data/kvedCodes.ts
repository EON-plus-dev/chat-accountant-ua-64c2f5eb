/**
 * Пошук КВЕД за кодом або назвою
 */
export const searchKved = (query: string, limit: number = 10): KvedCode[] => {
  const normalizedQuery = query.toLowerCase().trim();
  
  if (!normalizedQuery) {
    return KVED_CODES.slice(0, limit);
  }
  
  return KVED_CODES
    .filter(kved => 
      kved.code.includes(normalizedQuery) || 
      kved.name.toLowerCase().includes(normalizedQuery)
    )
    .slice(0, limit);
};

/**
 * Отримати КВЕД за кодом
 */
export const getKvedByCode = (code: string): KvedCode | undefined => {
  return KVED_CODES.find(kved => kved.code === code);
};

/**
 * Отримати назву секції
 */
export const getSectionName = (sectionCode: string): string => {
  return KVED_SECTIONS[sectionCode] || 'Інше';
};
