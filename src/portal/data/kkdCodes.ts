/**
 * Класифікація доходів бюджету (ККД) — коди, які вказуються у платіжках на сплату податків і зборів.
 * Джерело: Наказ Мінфіну № 11 «Про бюджетну класифікацію» (зі змінами).
 * Використання: реквізит «Призначення платежу» / поле «Код класифікації доходів бюджету» у платіжних
 * дорученнях SEPA-UA та у платіжках Казначейства; разом з IBAN бюджетного рахунку формує адресацію коштів.
 *
 * Структура коду: 8 цифр.
 *   1     — клас доходів (1 — податкові, 2 — неподаткові, 3 — операції з капіталом, 4 — трансферти)
 *   2-3   — підгрупа
 *   4-5   — стаття
 *   6-7   — підстаття
 *   8     — елемент (рівень бюджету: 0 — зведений, 1 — держбюджет, 2 — місцевий)
 */

export type KkdClass = 'tax' | 'non_tax' | 'capital' | 'transfer' | 'official';
export type KkdBudgetLevel = 'state' | 'local' | 'both';

export interface KkdEntry {
  /** 8-значний код ККД. */
  code: string;
  /** Коротка назва доходу (як у платіжці). */
  name: string;
  /** Розгорнута офіційна назва (як у Наказі № 11). */
  fullName?: string;
  class: KkdClass;
  budgetLevel: KkdBudgetLevel;
  /** Для якого податку/збору використовується (звʼязок із розділом «Бюджетні рахунки»). */
  taxKey?: string;
  /** Хто типово сплачує. */
  payers: string[];
  /** Короткий нейтральний коментар. */
  note?: string;
  /** Чи популярний код у щоденній практиці ФОП/ТОВ. */
  popular?: boolean;
}

