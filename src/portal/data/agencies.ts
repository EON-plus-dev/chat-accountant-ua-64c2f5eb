/**
 * Держоргани — довідник органів державної влади, з якими взаємодіє бізнес і фізособа.
 * Гарячі лінії, чат-боти, електронні кабінети, форми звернень, типові процедури.
 */

export type AgencyCategory =
  | 'tax' // ДПС, Мінфін, Держказначейство
  | 'social' // ПФУ, ФСС, Центр зайнятості
  | 'labor' // Держпраці
  | 'business_registration' // Мінʼюст, Держгеокадастр
  | 'digital_services' // Дія, Мінцифри
  | 'statistics' // Держстат
  | 'finance_regulator' // НБУ, НКЦПФР
  | 'customs' // Держмитна служба
  | 'consumer_protection' // Держпродспоживслужба
  | 'antimonopoly'; // АМКУ

export type AgencyAudience = 'business' | 'personal' | 'both';

export interface AgencyContact {
  type: 'hotline' | 'chatbot' | 'email' | 'cabinet' | 'address' | 'social';
  label: string;
  value: string;
  url?: string;
  /** Графік роботи, якщо актуально */
  schedule?: string;
}

export interface AgencyService {
  /** Назва послуги: «Реєстрація ФОП», «Подача декларації про доходи» тощо */
  name: string;
  description: string;
  /** Куди йти: URL кабінету, послуги Дії, форми */
  url?: string;
  /** Орієнтовний строк виконання */
  timeline?: string;
  /** Вартість (грн) або «безкоштовно» */
  cost?: string;
}

export interface AgencyEntry {
  id: string;
  slug: string;
  shortName: string; // ДПС, ПФУ, НБУ
  fullName: string;
  category: AgencyCategory;
  audience: AgencyAudience;

  /** EDRPOU держоргану */
  edrpou?: string;
  /** Голова / Керівник */
  head?: string;

  summary: string; // ≤ 220 символів
  mission: string; // що робить орган
  /** Кого регулює і за що відповідає */
  jurisdiction: string;

  /** Офіційний сайт */
  website: string;
  contacts: AgencyContact[];
  services: AgencyService[];

  /** Поширені помилки / лайфхаки взаємодії */
  tips?: string[];
  /** Червоні прапори: на що звертати увагу */
  warnings?: string[];

  relatedLawSlugs?: string[];
  relatedClarificationSlugs?: string[];
  relatedPenaltySlugs?: string[];
  relatedToolIds?: string[];

  tags: string[];
  faq?: { q: string; a: string }[];

  seoTitle?: string;
  seoDescription?: string;
  seoKeywords?: string[];
}

