export type IncomeCodeCategory =
  | 'salary'
  | 'cpd'
  | 'rent'
  | 'dividends'
  | 'gifts'
  | 'social'
  | 'inheritance'
  | 'investment'
  | 'fop'
  | 'foreign'
  | 'other';

export type Taxableness = 'taxable' | 'exempt' | 'partial';

export interface IncomeCodeEntry {
  /** 3-значний код ознаки доходу. */
  code: string;
  /** Коротка назва (як у класифікаторі). */
  name: string;
  /** Розгорнутий опис із прикладами. */
  description: string;
  category: IncomeCodeCategory;
  /** Чи оподатковується ПДФО (18%). */
  pdfo: Taxableness;
  /** Чи оподатковується військовим збором. */
  vz: Taxableness;
  /** Стаття ПКУ, що регулює. */
  pkArticle?: string;
  /** Хто типово виплачує. */
  payers: string[];
  /** Чи популярний у практиці. */
  popular?: boolean;
  /** Особливі коментарі (ліміти, винятки). */
  note?: string;
}

