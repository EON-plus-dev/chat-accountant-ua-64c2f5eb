/** Знайти випуск за id. */
export function getNewsletterIssueById(id: string): NewsletterIssue | undefined {
  return NEWSLETTER_ISSUES.find(i => i.id === id);
}

/** Сусідні випуски (новіший / старіший) у хронологічному порядку. */
export function getAdjacentIssues(id: string) {
  const idx = NEWSLETTER_ISSUES.findIndex(i => i.id === id);
  if (idx === -1) return { newer: undefined, older: undefined };
  return {
    newer: idx > 0 ? NEWSLETTER_ISSUES[idx - 1] : undefined,
    older: idx < NEWSLETTER_ISSUES.length - 1 ? NEWSLETTER_ISSUES[idx + 1] : undefined,
  };
}

/** Інші релевантні випуски (тієї ж категорії або найсвіжіші). */
export function getRelatedIssues(id: string, limit = 3): NewsletterIssue[] {
  const current = getNewsletterIssueById(id);
  const others = NEWSLETTER_ISSUES.filter(i => i.id !== id);
  if (!current) return others.slice(0, limit);
  const sameCategory = others.filter(i => i.category === current.category);
  const fillers = others.filter(i => i.category !== current.category);
  return [...sameCategory, ...fillers].slice(0, limit);
}
