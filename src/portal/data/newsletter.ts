export type NewsletterCategory = 'Податки' | 'ФОП' | 'Звітність' | 'Бізнес' | 'Право';

export interface NewsletterSection {
  /** Іконка-емодзі для секції — швидкий візуальний скан. */
  icon: string;
  /** Заголовок розділу. */
  title: string;
  /** Основний текст 2-4 речення. */
  body: string;
  /** Готова дія / порада в кінці розділу. */
  takeaway?: string;
  /** Першоджерела (закони, листи ДПС, роз'яснення). */
  sources?: { label: string; url: string }[];
  /** Пов'язана стаття на порталі. */
  articleId?: string;
  /** Пов'язаний інструмент FINTODO. */
  toolSlug?: string;
  toolLabel?: string;
}

export interface NewsletterIssue {
  id: string
  issue: number
  title: string
  date: string
  /** ISO-дата для розрахунків / SEO. */
  publishedAt: string
  summary: string
  /** Розширене вступне слово від редакції (1-2 абзаци). */
  intro: string
  highlights: string[]
  sections: NewsletterSection[]
  /** Особиста нотатка від редактора в кінці випуску. */
  editorNote?: string
  /** Підпис автора. */
  authorName?: string
  authorRole?: string
  articleIds: string[]
  subscribersAtTime: number
  /** Хв читання. */
  readingTime: number
  /** Тематична категорія для швидкого сканування архіву. */
  category?: NewsletterCategory
}

