/**
 * Довідник МФО / SWIFT українських банків.
 * Джерело: офіційний реєстр НБУ (bank.gov.ua), станом на 2026 рік.
 * Використання: заповнення платіжних доручень (МФО — старе позначення, фактично
 * перші 6 цифр IBAN), міжнародні платежі та ЗЕД (SWIFT/BIC), перевірка реквізитів
 * контрагентів. У сучасних IBAN UA МФО банку = позиції 5–10.
 */

export type BankStatus = 'active' | 'liquidating' | 'liquidated';
export type BankType = 'state' | 'commercial' | 'foreign_subsidiary' | 'cooperative';

export interface BankMfoEntry {
  /** Код банку (МФО) — 6 цифр, відповідають позиціям 5–10 IBAN UA. */
  mfo: string;
  /** Скорочена назва (як у платіжках). */
  shortName: string;
  /** Повна офіційна назва. */
  fullName: string;
  /** SWIFT/BIC (8 або 11 символів). Null якщо банк не має SWIFT. */
  swift: string | null;
  /** ЄДРПОУ юридичної особи. */
  edrpou: string;
  /** Сайт без https. */
  website?: string;
  /** Місто розташування головного офісу. */
  city: string;
  status: BankStatus;
  type: BankType;
  /** Чи популярний для бізнесу — для пріоритизації у пошуку. */
  popular?: boolean;
  /** Короткий нейтральний коментар (спеціалізація, особливості). */
  note?: string;
}

