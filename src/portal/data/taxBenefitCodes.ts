/**
 * Довідник податкових пільг ДПС — коди для Звіту про суми податкових пільг (ф. 1ПП)
 * та для зазначення в податкових деклараціях.
 *
 * Джерело: офіційний Довідник податкових пільг, що є втратами доходів бюджету,
 * який ДПС оновлює щокварталу (наказ ДПС). Snapshot: довідник № 124 / 125 (2025-2026).
 *
 * Повний довідник містить ~400 кодів. Тут зібрано ТОП-32 найчастіше застосовуваних:
 * ПДВ (197 / 195 / підрозділ 2 розд. XX), прибуток (142, IT-Парк, Дія.City),
 * ПДФО (165), плата за землю, ЄП. Кожен запис має код, назву пільги,
 * податок, ставку, статтю ПКУ, тип звітування у 1ПП і коротку довідку.
 *
 * Використання:
 *  - Звіт про суми податкових пільг (ф. 1ПП) — заповнюється щоквартально
 *  - Декларація з ПДВ (додаток 5) — коди звільнень
 *  - Декларація з податку на прибуток — пільгові операції
 */

export type TaxBenefitTax =
  | 'pdv'      // ПДВ
  | 'profit'   // Податок на прибуток підприємств
  | 'pdfo'     // ПДФО
  | 'land'     // Плата за землю
  | 'ep'       // Єдиний податок
  | 'rent';    // Рентна плата

export type TaxBenefitKind =
  | 'exempt'        // звільнення від оподаткування
  | 'reduced_rate'  // знижена ставка
  | 'zero_rate'     // нульова ставка
  | 'deduction'     // зменшення бази
  | 'not_subject';  // не є об'єктом

export type TaxBenefitCategory =
  | 'it'           // ІТ / Дія.City
  | 'medicine'     // ліки, медтехніка
  | 'education'    // освіта
  | 'social'       // гуманітарна, соц.захист
  | 'defense'      // оборона, ЗСУ
  | 'agro'         // сільське господарство
  | 'export'       // експорт
  | 'energy'       // енергоефективність
  | 'investment'   // інвестпроєкти
  | 'culture'      // культура, релігія
  | 'other';

export interface TaxBenefitCode {
  /** 7-значний код за довідником ДПС */
  code: string;
  /** Назва пільги */
  name: string;
  /** Податок, до якого застосовується */
  tax: TaxBenefitTax;
  /** Тип пільги */
  kind: TaxBenefitKind;
  /** Категорія для фільтрації */
  category: TaxBenefitCategory;
  /** Стаття ПКУ або іншого закону */
  legalRef: string;
  /** Ставка (для зниженої/нульової) */
  rate?: string;
  /** Коротка довідка — суть пільги */
  summary: string;
  /** Дата початку дії (з QXXXX/рік) */
  startDate?: string;
  /** Дата закінчення, якщо є */
  endDate?: string;
  /** Чи потрапляє у часто-використовувані */
  popular?: boolean;
  /** Чи потрібно подавати ф. 1ПП по цій пільзі */
  requires1pp: boolean;
}

