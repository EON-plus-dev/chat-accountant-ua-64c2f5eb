export type CustomsDocCategory =
  | 'customs_declaration'   // митні декларації
  | 'transport'             // транспортні (CMR, AWB, B/L)
  | 'commercial'            // комерційні (інвойс, проформа, пак.лист)
  | 'origin'                // походження (EUR.1, Form A, СТ-1)
  | 'permits'               // дозволи (ATA, ліцензії, сертифікати)
  | 'other';

export type CustomsDocAudience = 'import' | 'export' | 'both';

export interface CustomsDocument {
  code: string;
  name: string;
  slug: string;
  category: CustomsDocCategory;
  audience: CustomsDocAudience;
  issuer: string;            // хто оформлює / видає
  legalRef: string;          // нормативка
  purpose: string;           // призначення
  whenRequired: string;      // коли обовʼязковий
  retention: string;         // строк зберігання
  popular?: boolean;
  note?: string;
}

