export interface Certification {
  id: string;
  slug: string;
  code: string;             // напр. «ACCA», «ISO 9001»
  name: string;
  category: CertCategory;
  scope?: CertScope;        // якщо не задано — береться з CERT_CATEGORY_SCOPE[category]
  issuer: string;
  country: string;
  website: string;
  tagline: string;
  prerequisites: string;
  duration: string;
  examsCount: number;       // 0 для бізнес-сертів (аудити, не іспити)
  costUsd: string;          // діапазон у $ або у грн для бізнес-сертів
  recognizedIn: string;
  uaPartners: string[];     // де готують / акредитовані органи в Україні
  validityYears?: number;
  popular?: boolean;
  emoji: string;
  // Бізнес-специфічні (опційно):
  applicableTo?: string;            // «виробництво, експорт у ЄС»
  auditCycle?: string;              // «щорічний наглядовий, ре-сертифікація 3 р.»
  typicalCostUah?: string;          // «80 000 – 300 000 ₴ + 30–80 тис./рік»
  ukrainianBodies?: string[];       // акредитовані органи сертифікації в Україні
  mandatoryFor?: string;            // «обов'язково для експорту в ЄС»
  legalBasis?: string;              // нормативка
}

