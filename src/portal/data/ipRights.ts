export type IpRightKind =
  | 'trademark'   // торгові марки (знаки для товарів і послуг)
  | 'patent'      // винаходи, корисні моделі, промзразки
  | 'copyright'   // авторське право, суміжні права
  | 'license'     // ліцензійні договори
  | 'royalty'     // роялті, оподаткування
  | 'software'    // софт як IP, Дія City, опціони
  | 'protection'; // захист прав, суд, митниця

export interface IpRightEntry {
  id: string;
  slug: string;
  name: string;
  kind: IpRightKind;
  /** Короткий опис */
  summary: string;
  /** Орган реєстрації / захисту */
  authority?: string;
  /** Строк дії охорони */
  term?: string;
  /** Вартість офіційних зборів */
  cost?: string;
  /** Деталі / порядок дій */
  details: string[];
  legalRef: string;
  /** Оподаткування */
  taxation?: string;
  /** Типові ризики / поради */
  practicalNote?: string;
  popular?: boolean;
}

