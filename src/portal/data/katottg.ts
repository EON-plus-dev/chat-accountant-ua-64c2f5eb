export interface KatottgEntry {
  id: string;
  /** Повний код КАТОТТГ */
  code: string;
  /** URL-slug — нормалізована назва */
  slug: string;
  /** Офіційна назва одиниці */
  name: string;
  /** Тип у називному відмінку: «місто», «селище», «село», «область» */
  unitType: string;
  level: KatottgLevel;
  /** Код одиниці-батька (область для району, район для громади тощо) */
  parentCode?: string;
  /** Назва області — для швидкого фільтра */
  oblast: string;

  /** Поштовий індекс (для міст) */
  postalCode?: string;
  /** Орієнтовне населення (тис. осіб) */
  population?: number;
  /** Використання у звітності — короткий контекст */
  usageContext: string;
  audience: "business" | "personal" | "both";

  tags: string[];
  faq?: DirectoryFaqItem[];

  relatedAgencySlugs?: string[];
  relatedToolIds?: string[];

  seoTitle?: string;
  seoDescription?: string;
  seoKeywords?: string[];
}

