/**
 * Пільги і звільнення з ПДВ — структурований довідник за ст. 196, 197 ПКУ
 * та підзаконними актами КМУ.
 *
 * Типи звільнень:
 *   • не є об'єктом оподаткування (ст. 196 ПКУ) — операція взагалі поза ПДВ;
 *   • звільнені від ПДВ (ст. 197 ПКУ + переліки КМУ) — об'єкт є, але ставка 0% без
 *     права на податковий кредит;
 *   • нульова ставка (ст. 195 ПКУ) — експорт, міжнародні перевезення тощо, з правом на
 *     податковий кредит.
 *
 * Дані станом на квітень 2026 з урахуванням ЗУ № 4015-IX від 10.10.2024.
 */

