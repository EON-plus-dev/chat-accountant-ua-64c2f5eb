// Розгортає скорочення організаційно-правової форми у повну українську назву
// для відображення в шапці паспорту кабінету.
// Приклади:
//   "ФОП Іваненко Іван Іванович"  → "Фізична особа-підприємець Іваненко Іван Іванович"
//   'ТОВ "Технопром Груп"'        → 'Товариство з обмеженою відповідальністю "Технопром Груп"'
//   "Група ФОП «Консалтинг»"      → "Група фізичних осіб-підприємців «Консалтинг»"

const RULES: Array<{ re: RegExp; replace: string }> = [
  // Складні форми — спочатку
  { re: /^Група\s+ФОП\s+/u, replace: "Група фізичних осіб-підприємців " },
  // Прості префікси
  { re: /^ФОП\s+/u, replace: "Фізична особа-підприємець " },
  { re: /^ТОВ\s+/u, replace: "Товариство з обмеженою відповідальністю " },
  { re: /^ПрАТ\s+/u, replace: "Приватне акціонерне товариство " },
  { re: /^ПАТ\s+/u, replace: "Публічне акціонерне товариство " },
  { re: /^ПП\s+/u, replace: "Приватне підприємство " },
  { re: /^ФГ\s+/u, replace: "Фермерське господарство " },
  { re: /^ДП\s+/u, replace: "Державне підприємство " },
  { re: /^КП\s+/u, replace: "Комунальне підприємство " },
];

export function expandLegalName(name: string | undefined | null): string {
  if (!name) return "";
  const trimmed = name.trim();
  for (const rule of RULES) {
    if (rule.re.test(trimmed)) {
      return trimmed.replace(rule.re, rule.replace);
    }
  }
  return trimmed;
}
