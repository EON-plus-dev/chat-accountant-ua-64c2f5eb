/**
 * Discrepancy Act Types
 * 
 * Types for creating "Act of Discrepancy" (Акт розбіжностей)
 * when contractor disagrees with document terms
 */

export type DiscrepancyStatus = "draft" | "reviewed" | "accepted" | "rejected";

export interface DiscrepancyCard {
  id: string;
  clauseReference?: string;        // "п. 4.3", "Розділ III"
  originalText: string;            // Original text from document
  proposedText?: string;           // Proposed edit
  aiComment?: string;              // AI-generated comment/explanation
  userComment?: string;            // User's comment
  status: DiscrepancyStatus;
  createdAt: string;
  // For text highlighting in viewer
  textSelection?: {
    startOffset: number;
    endOffset: number;
  };
}

export interface DiscrepancyAct {
  id: string;
  originalDocumentId: string;      // ID of the document being disputed
  originalDocumentNumber: string;  // Document number for display
  originalDocumentType: string;    // contract, invoice, etc.
  cards: DiscrepancyCard[];
  summary?: string;                // AI-generated summary of all discrepancies
  status: "draft" | "sent" | "resolved";
  createdAt: string;
  createdBy: string;
  sentAt?: string;
  resolvedAt?: string;
}

export interface DiscrepancyAIChatMessage {
  id: string;
  role: "user" | "assistant";
  content: string;
  timestamp: string;
  suggestedCard?: Partial<DiscrepancyCard>;  // AI can suggest a card to add
}

